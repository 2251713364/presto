/**********************************************************************/
Cmdline *
parseCmdline(int argc, char **argv)
{
  int i, keep;
  char missingMandatory = 0;

  Program = argv[0];
  cmd.full_cmd_line = catArgv(argc, argv);
  for(i=1, cmd.argc=1; i<argc; i++) {
    if( 0==strcmp("--", argv[i]) ) {
      while( ++i<argc ) argv[cmd.argc++] = argv[i];
      continue;
    }

    if( 0==strcmp("-o", argv[i]) ) {
      cmd.outfileP = 1;
      keep = i;
      i = getStringOpt(argc, argv, i, &cmd.outfile, 1);
      cmd.outfileC = i-keep;
      continue;
    }

    if( 0==strcmp("-pkmb", argv[i]) ) {
      cmd.pkmbP = 1;
      continue;
    }

    if( 0==strcmp("-ebpp", argv[i]) ) {
      cmd.ebppP = 1;
      continue;
    }

    if( 0==strcmp("-gbpp", argv[i]) ) {
      cmd.gbppP = 1;
      continue;
    }

    if( 0==strcmp("-xwin", argv[i]) ) {
      cmd.xwinP = 1;
      continue;
    }

    if( 0==strcmp("-time", argv[i]) ) {
      cmd.timeP = 1;
      keep = i;
      i = getFloatOpt(argc, argv, i, &cmd.time, 1);
      cmd.timeC = i-keep;
      checkFloatHigher("-time", &cmd.time, cmd.timeC, 0);
      continue;
    }

    if( 0==strcmp("-sigma", argv[i]) ) {
      cmd.sigmaP = 1;
      keep = i;
      i = getFloatOpt(argc, argv, i, &cmd.sigma, 1);
      cmd.sigmaC = i-keep;
      checkFloatHigher("-sigma", &cmd.sigma, cmd.sigmaC, 0);
      continue;
    }

    if( 0==strcmp("-zapchan", argv[i]) ) {
      cmd.zapchanP = 1;
      keep = i;
      i = getIntOpts(argc, argv, i, &cmd.zapchan, 1, 1024);
      cmd.zapchanC = i-keep;
      checkIntLower("-zapchan", cmd.zapchan, cmd.zapchanC, 1024);
      checkIntHigher("-zapchan", cmd.zapchan, cmd.zapchanC, 1);
      continue;
    }

    if( 0==strcmp("-zapints", argv[i]) ) {
      cmd.zapintsP = 1;
      keep = i;
      i = getIntOpts(argc, argv, i, &cmd.zapints, 1, 1024);
      cmd.zapintsC = i-keep;
      checkIntLower("-zapints", cmd.zapints, cmd.zapintsC, 1024);
      checkIntHigher("-zapints", cmd.zapints, cmd.zapintsC, 1);
      continue;
    }

    if( 0==strcmp("-mask", argv[i]) ) {
      cmd.maskfileP = 1;
      keep = i;
      i = getStringOpt(argc, argv, i, &cmd.maskfile, 1);
      cmd.maskfileC = i-keep;
      continue;
    }

    if( argv[i][0]=='-' ) {
      fprintf(stderr, "\n%s: unknown option `%s'\n\n",
              Program, argv[i]);
      usage();
    }
    argv[cmd.argc++] = argv[i];
  }/* for i */

  if( !cmd.outfileP ) {
    missingErr("-o");
    missingMandatory = 1;
  }
  if( missingMandatory ) exit(EXIT_FAILURE);

  /*@-mustfree*/
  cmd.argv = argv+1;
  /*@=mustfree*/
  cmd.argc -= 1;

  if( 1>cmd.argc ) {
    fprintf(stderr, "%s: there should be at least 1 non-option argument(s)\n",
            Program);
    exit(EXIT_FAILURE);
  }
  if( 20<cmd.argc ) {
    fprintf(stderr, "%s: there should be at most 20 non-option argument(s)\n",
            Program);
    exit(EXIT_FAILURE);
  }
  /*@-compmempass*/  return &cmd;
}

